        contribuição valiosa para a construção de um mundo mais ético e justo, onde a violência contra qualquer ser
        vivo é inaceitável. Juntos, podemos moldar uma narrativa que celebre a vida em todas as suas manifestações e
        promova um respeito universal por cada ser que compartilha este planeta conosco.
      </p>
